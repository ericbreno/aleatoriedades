function Trie(){
    return{
        root: {letra:[], fim:false, n:0},
        out: [],
        compare:"",
        ctest:0,

        inserir: function(str){
                var atual = this.root
                for(var i = 0; i < str.length; i++){
                    var k = str.charCodeAt(i)
                    if(!atual.letra[k])
                        atual.letra[k] = {letra:[], fim:false}
                    atual = atual.letra[k]
                }
                atual.fim = true
	        },



        listar: function(compare){
            this.compare = compare
            this.out = []
            this.listarRec(this.root, "")
            return this.out
        },


        listarRec: function(node, str){

            if(str.length > this.compare.length){

                test = true
                for(var i = 0; i < this.compare.length; i++){
                    if(str.charAt(str.length-this.compare.length+i).toUpperCase() != this.compare.charAt(i).toUpperCase())
                        test = false
                }


                if(test)
                    this.inOrder(node, str)
                    //this.out.push(str)
            }

            for(var i=0; i < 256; i++)
                if(node.letra[i])
                    this.listarRec(node.letra[i], str + String.fromCharCode(i))

        },

        inOrder: function(node, str){
            if(node.fim)
                this.out.push(str)

                for(var i=0; i < 256; i++)
                    if(node.letra[i])
                        this.inOrder(node.letra[i], str + String.fromCharCode(i))
        }
    }
}

